# Faça um Programa que peça as 4 notas bimestrais e mostre a média.

nota01 = float(input("Digite o valor da 1° nota"))
nota02 = float(input("Digite o valor da 2° nota"))
nota03 = float(input("Digite o valor da 3° nota"))
nota04 = float(input("Digite o valor da 4° nota"))

media = (nota01 + nota02 + nota03 + nota04) / 4

print(("A média bimestral foi" , media))